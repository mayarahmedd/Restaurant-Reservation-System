    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        User user = null;
        List<User> userlist = restaurant.getUsers().getUsers();
        for (int i = 0; i < userlist.size(); i++) {
            if (userlist.get(i).getUsername().equalsIgnoreCase(jTextField1.getText()) && userlist.get(i).getPassword().equals(jPasswordField1.getText())) {
                user = userlist.get(i);
                break;
            }
        }
        if (user != null) {
            Person person = PersonFactory.convertFromUser(user);
            if (person != null) {
                person.drawWindow(restaurant, savedrestaurant, user.getName());
                this.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Invalid username or password");
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

